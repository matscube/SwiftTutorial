    override func tableView(tableView: UITableView, cellForRowAtIndexPath indexPath: NSIndexPath) -> UITableViewCell {
        let cell = tableView.dequeueReusableCellWithIdentifier("reuseIdentifier", forIndexPath: indexPath) as UITableViewCell
        
        let section = indexPath.section
        let row = indexPath.row
        cell.textLabel?.text = "\(section)-\(row)"
        cell.detailTextLabel?.text = "\(section)"
        
        var label = UILabel()
        label.frame = CGRectMake(50, 0, 250, cell.frame.height)
        label.textAlignment = NSTextAlignment.Center
        label.center.y = cell.frame.height / 2
        label.text = "Cell descriptions"
        cell.contentView.addSubview(label)

        // Configure the cell...

        return cell
    }
    
    override func tableView(tableView: UITableView, titleForHeaderInSection section: Int) -> String? {
        return "Section: \(section)"
    }
    
    override func tableView(tableView: UITableView, didSelectRowAtIndexPath indexPath: NSIndexPath) {
        let section = indexPath.section
        let row = indexPath.row
        selectedSection = section
        selectedRow = row
        
        self.performSegueWithIdentifier("ModalDetailViewSegue", sender: nil)
    }
    
    private var selectedSection: Int?
    private var selectedRow: Int?
    override func prepareForSegue(segue: UIStoryboardSegue, sender: AnyObject?) {
        if (segue.identifier == "ModalDetailViewSegue") {
          var detailVC = segue.destinationViewController as DetailViewController
            detailVC.section = selectedSection
            detailVC.row = selectedRow
        }
    }
    

